package com.customersService.customersService.model;

public enum DeviceStatus {
	 ACTIVE,         // The device is operational and in regular use.
	    INACTIVE,       // The device is not currently in use or is idle.
	    OFFLINE,        // The device is disconnected from the network.
	    LOW_BATTERY,    // The device has low battery or power.
	    MAINTENANCE,    // The device is undergoing maintenance.
	    FAULTY,         // The device is not functioning correctly.
	    UNKNOWN  

}
